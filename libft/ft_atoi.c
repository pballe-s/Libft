#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	res;
	int	sinal;

	res = 0;
	i = 0;
	sinal = 1;
	while ((*(str + i) >= '\t' && *(str + i) <= '\r') || *(str + i) == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sinal *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (str[i] - '0');
		i++;
	}
	return (sinal * res);
}
